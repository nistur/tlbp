#ifndef __TLBP_TYPES_H__
#define __TLBP_TYPES_H__

//--------------------------------------------------------------------------------
// standard types
//--------------------------------------------------------------------------------
typedef unsigned char  u8;
typedef unsigned short u16;
typedef unsigned int   u32;
typedef unsigned long  u64;

typedef signed char    s8;
typedef signed short   s16;
typedef signed int     s32;
typedef signed long    s64;

//--------------------------------------------------------------------------------
// bitpacked types
//--------------------------------------------------------------------------------
typedef unsigned char  bu8;
typedef unsigned short bu16;
typedef unsigned int   bu32;
typedef unsigned long  bu64;

typedef signed char    bs8;
typedef signed short   bs16;
typedef signed int     bs32;
typedef signed long    bs64;

#endif/*__TLBP_TYPES_H__*/